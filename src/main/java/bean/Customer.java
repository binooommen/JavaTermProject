package bean;

/**
 *
 * @author c0647015
 */
public class Customer {
    private int id;
    private String create_date;
    private String name;

    public Customer() {
    }

    public Customer(int id, String create_date, String name) {
        this.id = id;
        this.create_date = create_date;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCreate_date() {
        return create_date;
    }

    public void setCreate_date(String create_date) {
        this.create_date = create_date;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
}
